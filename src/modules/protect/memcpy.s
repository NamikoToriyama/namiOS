memcpy:
        ; ---------------------------------
        ; スタックフレームの構築
        ; ---------------------------------

        push    ebp
        mov     ebp, esp

        ; ---------------------------------
        ; レジスタの保存
        ; ---------------------------------
        push    ecx
        push    esi
        push    edi

        ; ---------------------------------
        ; バイト単位でのコピー
        ; ---------------------------------
        cld
        mov     edi, [ebp + 4]
        mov     esi, [ebp + 6]
        mov     ecx, [ebp + 8]

        rep     movsb      

        ; ---------------------------------
        ; レジスタの復帰
        ; ---------------------------------
        pop     edi
        pop     esi
        pop     ecx

        ; ---------------------------------
        ; スタックフレームの破棄
        ; ---------------------------------
        mov     esp, ebp
        pop     ebp

        ret
